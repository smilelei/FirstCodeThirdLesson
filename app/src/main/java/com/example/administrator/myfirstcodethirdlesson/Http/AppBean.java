public class AppBean {
    public String name;
    public int version;
    public int id;

    public String getName() {
        return name;
    }

    public void setApp(String name) {
        this.name = name;
    }

    public int getVersion() {
        return version;
    }

    public void setVersion(int version) {
        this.version = version;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
